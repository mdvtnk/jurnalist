// –ü—Ä–∏–º–µ–Ω–µ–Ω–∏–µ —Ñ–∏–ª—å—Ç—Ä–æ–≤
function applyFilters() {
  const cards = document.querySelectorAll('.card');
  const searchValue = document.getElementById('search').value.toLowerCase().trim();
  
  cards.forEach(card => {
    const law = laws.find(l => l.id === card.id);
    if (!law) return;
    
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞—Ç–µ–≥–æ—Ä–∏—é
    const categoryActive = activeCategories[law.category];
    
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–∏—Å–∫
    const matchesSearch = !searchValue || card.innerText.toLowerCase().includes(searchValue);
    
    // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –∫–∞—Ä—Ç–æ—á–∫—É —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –∫–∞—Ç–µ–≥–æ—Ä–∏—è –∞–∫—Ç–∏–≤–Ω–∞ –∏ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –ø–æ–∏—Å–∫—É
    if (categoryActive && matchesSearch) {
      card.style.display = 'block';
      card.style.animation = 'fadeInScale 0.3s ease-out';
    } else {
      card.style.display = 'none';
    }
  });
}

