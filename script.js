  // –î–æ–±–∞–≤–ª—è–µ–º —ç–º–æ–¥–∑–∏ –≤ –∑–∞–≥–æ–ª–æ–≤–æ–∫
  modalTitle.innerHTML = `<span class="modal-emoji">${emoji}</span> ${law.title}`;
  
