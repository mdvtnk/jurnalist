      activeCategories[category] = !activeCategories[category];
      link.classList.toggle('active', activeCategories[category]);
      
      // –ü—Ä–∏–º–µ–Ω—è–µ–º —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏—é
      applyFilters();
